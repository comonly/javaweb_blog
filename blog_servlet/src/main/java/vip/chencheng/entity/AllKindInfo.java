package vip.chencheng.entity;

public class AllKindInfo {
	private int akid;
	private String akvalue;
	private String akdetail;
	private int akstate;
	private int akfather;
	private int akkind;
	


	public int getAkid() {
		return akid;
	}
	public void setAkid(int akid) {
		this.akid = akid;
	}
	public String getAkvalue() {
		return akvalue;
	}
	public void setAkvalue(String akvalue) {
		this.akvalue = akvalue;
	}
	public String getAkdetail() {
		return akdetail;
	}
	public void setAkdetail(String akdetail) {
		this.akdetail = akdetail;
	}
	public int getAkstate() {
		return akstate;
	}
	public void setAkstate(int akstate) {
		this.akstate = akstate;
	}
	public int getAkfather() {
		return akfather;
	}
	public void setAkfather(int akfather) {
		this.akfather = akfather;
	}
	public int getAkkind() {
		return akkind;
	}
	public void setAkkind(int akkind) {
		this.akkind = akkind;
	}

}
